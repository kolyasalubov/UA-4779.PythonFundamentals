def division (a,b: float) -> float:
    """
    Function divides first number by second
    """
    return a/b

def addition (a,b: float) -> float:
    """
    Function adds first number to second
    """
    return a+b

def multiplication (a,b: float) -> float:
    """
    Function multiplies first number times second
    """
    return a*b

def subtraction (a,b: float) -> float:
    """
    Function subtracts first number from second
    """
    return a-b
